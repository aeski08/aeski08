package DoWhile;

public class DoWhileExample6 {

	public static void main(String[] args) {
		// 1 to 10 output 1 10 2 9 3 8 4 7 5 6
         int i,k;
	
		
		for(i = 1, k=10; i<=5;  i++, k--) {
			
			
				
			System.out.println("i = " + i);	
			
			System.out.println("k = " + k);
			
		}
		
	}	
	}	
	
	


